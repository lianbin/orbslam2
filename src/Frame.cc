//将图像的四个角进行畸变矫正。
//然后将图像分成64 *48的网格
//将特征点进行畸变矫正，将畸变矫正后的坐标，分配到64 *48的网格
//对应的网格之中
void Frame::AssignFeaturesToGrid()
{
    int nReserve = 0.5f*N/(FRAME_GRID_COLS*FRAME_GRID_ROWS);
    for(unsigned int i=0; i<FRAME_GRID_COLS;i++)
        for (unsigned int j=0; j<FRAME_GRID_ROWS;j++)
            mGrid[i][j].reserve(nReserve);

    for(int i=0;i<N;i++)
    {
        const cv::KeyPoint &kp = mvKeysUn[i];//畸变矫正后的特征点所在的图像坐标
        int nGridPosX, nGridPosY;
        if(PosInGrid(kp,nGridPosX,nGridPosY))        //判断特征点是否在64 * 48个网格内
            mGrid[nGridPosX][nGridPosY].push_back(i);//存储落入网格内的特征点编号
    }
}

